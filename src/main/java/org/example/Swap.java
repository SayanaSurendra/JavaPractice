package org.example;

public class Swap {
    public static void main(String[] args) {
        int a,b,temp;
         a=10;
        b=20;

        int chr='Z';
        System.out.println(chr);

        System.out.println("Before swapping: a = "+ a+ " " +"b ="+b );
        temp=a;
        a=b;
        b=temp;
        System.out.println("after swapping: a = "+ a+ " " +"b ="+b );

        System.out.println();

    }
}
